/**
 * Создает системные промпты в БД (если их еще нет)
 */
export async function createSystemPrompts() {
  const { prisma } = await import('../prisma');

  for (const [key, promptData] of Object.entries(SYSTEM_PROMPTS)) {
    const existing = await prisma.prompt.findUnique({
      where: { key }
    });

    if (!existing) {
      await prisma.prompt.create({
        data: {
          key: promptData.key,
          name: promptData.name,
          description: promptData.description,
          content: promptData.content,
          isActive: true
        }
      });
      console.log(`[SystemPrompts] Created prompt: ${key}`);
    } else {
      // Обновляем существующий промпт, если он изменился
      if (existing.content !== promptData.content) {
        await prisma.prompt.update({
          where: { key },
          data: {
            name: promptData.name,
            description: promptData.description,
            content: promptData.content
          }
        });
        console.log(`[SystemPrompts] Updated prompt: ${key}`);
      }
    }
  }
}

